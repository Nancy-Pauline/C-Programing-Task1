#include <stdio.h>
#include <stdlib.h>

int main()
{
    int age = 20;
    float height = 5.9;
    double distance = 12345.6789;
    char initial = 'A';

    printf("The age is %d\n", age);
    printf("The height is %f\n", height);
    printf("The distance is %lf\n", distance);
    printf("The initial is %c\n", initial);
    return 0;
}
